FONT_NAME = "Times New Roman"
FONT_SIZE_BODY = Pt(14)
LINE_SPACING = 1.5
FIRST_LINE_INDENT = Cm(1.25)


def _set_run(run, size=FONT_SIZE_BODY, bold=False, italic=False):
    run.font.name = FONT_NAME
    run.font.size = size
    run.font.bold = bold
    run.font.italic = italic
    # Принудительно ставим Times New Roman для кириллицы
    rPr = run._element.get_or_add_rPr()
    rFonts = rPr.find(qn("w:rFonts"))
    if rFonts is None:
        rFonts = OxmlElement("w:rFonts")
        rPr.append(rFonts)
    rFonts.set(qn("w:ascii"), FONT_NAME)
    rFonts.set(qn("w:hAnsi"), FONT_NAME)
    rFonts.set(qn("w:cs"), FONT_NAME)
    rFonts.set(qn("w:eastAsia"), FONT_NAME)


def add_paragraph(doc, text, *, bold=False, italic=False, align=None,
                   size=FONT_SIZE_BODY, indent=True, line_spacing=LINE_SPACING):
    p = doc.add_paragraph()
    if align is not None:
        p.alignment = align
    p.paragraph_format.line_spacing = line_spacing
    if indent:
        p.paragraph_format.first_line_indent = FIRST_LINE_INDENT
    run = p.add_run(text)
    _set_run(run, size=size, bold=bold, italic=italic)
    return p


def add_heading(doc, text, level=1):
    h = doc.add_heading("", level=level)
    h.paragraph_format.line_spacing = 1.0
    run = h.add_run(text)
    size = {0: Pt(20), 1: Pt(16), 2: Pt(14), 3: Pt(13)}.get(level, Pt(14))
    _set_run(run, size=size, bold=True)
    # Чёрный цвет для заголовков (иначе Word ставит синий)
    from docx.shared import RGBColor
    run.font.color.rgb = RGBColor(0, 0, 0)
    return h


def add_bullet(doc, text):
    p = doc.add_paragraph(style="List Bullet")
    p.paragraph_format.line_spacing = LINE_SPACING
    run = p.add_run(text)
    _set_run(run)
    return p


def add_numbered(doc, text):
    p = doc.add_paragraph(style="List Number")
    p.paragraph_format.line_spacing = LINE_SPACING
    run = p.add_run(text)
    _set_run(run)
