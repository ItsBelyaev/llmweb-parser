FONT_NAME = "Times New Roman"
SIZE_BODY = Pt(14)
LS_BODY = 1.5
FIRST_INDENT = Cm(1.25)


def _font(run, size=SIZE_BODY, bold=False, italic=False):
    run.font.name = FONT_NAME
    run.font.size = size
    run.font.bold = bold
    run.font.italic = italic
    rPr = run._element.get_or_add_rPr()
    rFonts = rPr.find(qn("w:rFonts"))
    if rFonts is None:
        rFonts = OxmlElement("w:rFonts")
        rPr.append(rFonts)
    for k in ("w:ascii", "w:hAnsi", "w:cs", "w:eastAsia"):
        rFonts.set(qn(k), FONT_NAME)


def add_p(doc, text, *, bold=False, italic=False, align=None,
          size=SIZE_BODY, indent=True, ls=LS_BODY):
    p = doc.add_paragraph()
    if align is not None:
        p.alignment = align
    p.paragraph_format.line_spacing = ls
    if indent:
        p.paragraph_format.first_line_indent = FIRST_INDENT
    run = p.add_run(text)
    _font(run, size=size, bold=bold, italic=italic)
    return p


def h(doc, text, level=1):
    hd = doc.add_heading("", level=level)
    hd.paragraph_format.line_spacing = 1.0
    run = hd.add_run(text)
    size = {0: Pt(20), 1: Pt(16), 2: Pt(14), 3: Pt(13)}.get(level, Pt(14))
    _font(run, size=size, bold=True)
    run.font.color.rgb = RGBColor(0, 0, 0)
    return hd


def li(doc, text, kind="bullet"):
    style = "List Bullet" if kind == "bullet" else "List Number"
    p = doc.add_paragraph(style=style)
    p.paragraph_format.line_spacing = LS_BODY
    run = p.add_run(text)
    _font(run)
    return p


def page_break(doc):
    p = doc.add_paragraph()
    p.add_run().add_break(WD_BREAK.PAGE)


