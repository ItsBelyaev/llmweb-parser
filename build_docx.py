# --- 3.11 Выявленные проблемы --------------------------------------------- #

add_heading(doc, "3.11 Выявленные проблемы и их решения", level=2)

add_paragraph(doc,
    "В процессе разработки и тестирования был выявлен ряд проблем, для каждой "
    "из которых найдено инженерное решение."
)

add_paragraph(doc,
    "Первая проблема — депрекация эндпоинта api-inference.huggingface.co. "
    "В апреле 2026 года Hugging Face прекратил поддержку старого "
    "Inference API и перешёл на новый routing-эндпоинт router.huggingface.co. "
    "Версии huggingface-hub до 1.0 продолжали обращаться по старому адресу и "
    "получали DNS-ошибку. Решением стал апгрейд huggingface-hub до версии "
    "1.16, которая корректно работает с новым роутером, и проектирование "
    "двухуровневого fallback на случай повторных миграций."
)

add_paragraph(doc,
    "Вторая проблема — обработка динамического контента на JavaScript-фреймворках. "
    "Некоторые сайты используют ленивую загрузку компонентов, и Playwright "
    "иногда завершал загрузку до полной отрисовки всех элементов. Решением "
    "стало использование события networkidle для ожидания затухания сетевой "
    "активности и дополнительной задержки в 2 секунды после него для "
    "выполнения отложенных скриптов."
)

add_paragraph(doc,
    "Третья проблема — антибот-системы. Сайты с защитой Cloudflare или "
    "аналогичными системами детектируют Playwright по флагу navigator.webdriver "
    "и блокируют доступ. Реализована ротация User-Agent строк и отключение "
    "флага через init_script, что позволяет проходить базовые проверки."
)

add_paragraph(doc,
    "Четвёртая проблема — размер HTML-документов. Даже после очистки некоторые "
    "страницы превышают контекстное окно языковых моделей. Реализовано "
    "адаптивное усечение с приоритизацией структурных элементов в верхней "
    "части DOM-дерева, где обычно располагается основной контент карточки товара."
)

add_paragraph(doc,
    "Пятая проблема — галлюцинации LLM при генерации CSS-селекторов. Модель "
    "иногда выдумывала несуществующие имена CSS-классов или возвращала "
    "невалидные селекторы. Решением стала автоматическая валидация: каждый "
    "сгенерированный селектор проверяется на исходном HTML через BeautifulSoup. "
    "Если селектор синтаксически некорректен или ничего не находит, он "
    "сбрасывается в null, а в markdownComments разметки добавляется "
    "соответствующее предупреждение."
)

