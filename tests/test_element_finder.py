"""Тесты эвристического поиска интерактивных элементов
(app/scraper/element_finder.py).

Цель — проверить scoring system на разнообразных кнопках, которые
встречаются на реальных интернет-магазинах: русские, английские,
с aria-label, с data-action, с классами BEM, с авто-сгенерированными
CSS-in-JS классами, с anti-pattern'ами («в избранное», «wishlist»).
"""

from app.scraper.element_finder import (
    MIN_CONFIDENT_SCORE,
    build_selector,
    find_best,
    find_candidates,
    _looks_autogenerated,
)
from bs4 import BeautifulSoup


# ─── Базовые сценарии ────────────────────────────────────────────────────────


def test_finds_obvious_russian_button():
    """Кнопка <button>В корзину</button> с явным классом должна найтись с большим score."""
    html = """
    <html><body>
      <button class="add-to-cart-btn" data-action="add-to-cart">В корзину</button>
    </body></html>
    """
    best = find_best(html, "add_to_cart")
    assert best is not None
    assert best.score >= MIN_CONFIDENT_SCORE
    assert "В корзину" in best.text or "корзину" in best.text.lower()


def test_finds_english_button():
    """Английская кнопка 'Add to cart' через aria-label на button."""
    html = """
    <html><body>
      <button class="product-buy-button" aria-label="Add to cart">
        <img src="x.jpg" alt="Cart">
      </button>
    </body></html>
    """
    best = find_best(html, "add_to_cart")
    assert best is not None
    assert best.score >= MIN_CONFIDENT_SCORE


def test_ignores_wishlist_button():
    """«В избранное» / Add to wishlist не должны побеждать."""
    html = """
    <html><body>
      <button class="wishlist-add">Add to wishlist</button>
      <button class="add-to-cart-btn">В корзину</button>
    </body></html>
    """
    best = find_best(html, "add_to_cart")
    assert best is not None
    assert "корзин" in best.text.lower()
    assert "wishlist" not in best.text.lower()


def test_no_button_returns_none():
    """Страница без подходящей кнопки — None."""
    html = """
    <html><body>
      <h1>Просто статья</h1>
      <p>Никаких кнопок тут нет</p>
      <a href="/about">О нас</a>
    </body></html>
    """
    assert find_best(html, "add_to_cart") is None


def test_disabled_button_low_score():
    """Disabled-кнопка должна сильно проседать в score."""
    html = """
    <html><body>
      <button class="add-to-cart" disabled>В корзину</button>
      <button class="add-to-cart-active">В корзину</button>
    </body></html>
    """
    candidates = find_candidates(html, "add_to_cart", limit=5)
    disabled_score = next(c.score for c in candidates if "disabled" in " ".join(c.explanation).lower() or any("disabled" in e for e in c.explanation))
    active_score = next(c.score for c in candidates if not any("disabled" in e for e in c.explanation))
    assert active_score > disabled_score


