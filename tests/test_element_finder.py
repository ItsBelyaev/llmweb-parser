# ─── build_selector ──────────────────────────────────────────────────────────


def test_build_selector_uses_id():
    soup = BeautifulSoup('<button id="addToCart">Buy</button>', "lxml")
    sel = build_selector(soup.find("button"))
    assert sel == "#addToCart"


def test_build_selector_skips_autogen_class():
    """Auto-generated класс типа css-1a2b3c должен игнорироваться."""
    soup = BeautifulSoup(
        '<button class="css-1a2b3c real-class">Buy</button>', "lxml"
    )
    sel = build_selector(soup.find("button"))
    assert "css-1a2b3c" not in sel
    assert "real-class" in sel


def test_build_selector_uses_data_testid():
    """data-testid имеет приоритет если нет нормального id."""
    soup = BeautifulSoup(
        '<button data-testid="cart-add-btn">Buy</button>', "lxml"
    )
    sel = build_selector(soup.find("button"))
    assert "data-testid" in sel
    assert "cart-add-btn" in sel


def test_build_selector_input_uses_name():
    soup = BeautifulSoup(
        '<input type="submit" name="add_to_cart" value="Add">', "lxml"
    )
    sel = build_selector(soup.find("input"))
    assert sel == 'input[name="add_to_cart"]'


# ─── _looks_autogenerated ────────────────────────────────────────────────────


def test_autogen_css_in_js():
    assert _looks_autogenerated("css-1a2b3c") is True
    assert _looks_autogenerated("sc-aBcD1") is True
    assert _looks_autogenerated("jsx-1234567890") is True
    assert _looks_autogenerated("MuiButton-root-12345") is True
    assert _looks_autogenerated("") is True


def test_autogen_keeps_normal_classes():
    assert _looks_autogenerated("add-to-cart") is False
    assert _looks_autogenerated("product-buy-button") is False
    assert _looks_autogenerated("btn-primary") is False
    assert _looks_autogenerated("hero__title") is False


