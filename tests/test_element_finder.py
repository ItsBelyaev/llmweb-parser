def test_candidates_sorted_by_score():
    """Возвращаемые кандидаты должны быть отсортированы по убыванию score."""
    html = """
    <html><body>
      <button class="buy">Купить</button>
      <button class="add-to-cart-btn" data-action="add-to-cart">В корзину</button>
      <button>Show more</button>
    </body></html>
    """
    candidates = find_candidates(html, "add_to_cart", limit=5)
    assert len(candidates) >= 2
    scores = [c.score for c in candidates]
    assert scores == sorted(scores, reverse=True)
